<?php
    /* Insira o valor 5 em uma variável, e o valor 3 em outra. 
    Teste os operadores de igualdade, diferença, idêntico e não idêntico.*/
    
    $a = 5;
    $b = "3";

    if ($a == $b) {
        echo "$a == $b <br>";
    }

    if ($a != $b) {
        echo "$a != $b <br>";
    }

    if ($a === $b) {
        echo "$a === $b <br>";
    }

    if ($a !== $b) {
        echo "$a !== $b <br>";
    }